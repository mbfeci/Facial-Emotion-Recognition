testDataDir = fullfile(base, 'data_noise_test'); 

[trainImages, trainLabels] = getImagesAndLabels(trainDataDir);
[testImages, testLabels] = getImagesAndLabels(testDataDir);

trainData = imageDatastore(trainImages);
